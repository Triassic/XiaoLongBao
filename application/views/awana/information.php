            <h2>Parental Support for Positive Discipline</h2>
            <hr>
            <p>The purpose of discipline is to teach Christ-like behavior, not merely a set of rules. To discipline is to teach or mold. 
            Discipline is done for your child. By setting a model of Christ-like conduct at home, clubbers will see the reinforcement of 
            AWANA standards in you.</p>

            <p>In AWANA, all leaders are trained to use positive discipline which encourages good behavior through rewards, such as verbal praise, 
                extra points and special recognition. We will never emphasize punishment for misbehavior.</p>

            <p>We use a Five-Count to bring order during activities. The Commander or Director will count 5-4-3-2-1. Each clubber should become 
                quiet and listen to instructions.</p>

            <p>However, when a child is deliberately misbehaving or distracting other clubbers, we will administer the Tree-Count rule which is 
                listed below:
                <ul>1st strike: a warning</ul>
                <ul>2nd strike: talk with Director or Commander</ul>
                <ul>3rd strike: must sit out for the rest of AWANA night and parents are called.</ul>
            </p>


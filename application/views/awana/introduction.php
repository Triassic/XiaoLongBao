        <h2>What is Awana</h2>
        <hr>
        <p>
        Awana is an international, Bible-centered children's ministry providing local churches with weekly clubs, programs and training. 
        Awana's goal is to equip churches to reach children and their families with the gospel of Christ and train them to serve Him. 
        The acronym Awana comes from the first letters of Approved Workmen Are Not Ashamed as taken from 2 Timothy 2:15.
        </p>
        </p>
        Ten standards of excellence for Awana Clubs are listed below:
        </p>
        <ol>
            <li>We reach boys and girls with the gospel of Christ and train them to serve Him.</li>
            <li>We recruit, train and motivate leaders to serve the local church.</li>
            <li>We use positive discipline and maintain order throughout the entire club meeting.</li>
            <li>We attract children with games and activities.</li>
            <li>We encourage memorization, understanding and application of Scripture.</li>
            <li>We use the elements of Council Time effectively.</li>
            <li>We motivate children through incentives.</li>
            <li>We manage God's resources with faithfulness and integrity.</li>
            <li>We minister to parents and family members.</li>
            <li>We magnify the impact of our church's ministry through cooperation with national AWANA ministry.</li>
        </ol>


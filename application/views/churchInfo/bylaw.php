	<ul>第三條：每屆年度新舊長執交接後，財務組應要求各組造具全年度預算，交由財務會計彙編全教會新年度總預算，提至長執會審查通過後，再提交會友大會通過後實施，以期會友盡力奉獻。</ul>
	<ul>第四條：每屆年度結束後，如有盈餘庫存，應半數移入建堂及維修基金帳戶內儲存，另一半移入預備金項下。</ul>
	<ul>第五條：在年度尚未結束前，如某項科目預算已用罄，需要追加預算，或有其他事故必須支付者，經長執會通過後，可在教會預備金項下撥付，或由長執會主席報告會友舉行特別奉獻，以資挹注。</ul>
	<ul>第六條：本教會對外一切重要行文及簽立契約、憑證等文件，經長執會或會友大會通過後，均以教會名義行之，由主任牧師及長執會正副主席共同簽署。</ul>
	<ul>第七條：長執們如有感聖工日漸繁重，有增加人手之必要，可徵得主席同意，各自邀請會友襄助，組成工作小組，配搭推行聖工。</ul>
	<ul>第八條：長執會開會討論議案以三分之二通過為原則。</ul>


	<a id="Chapter9"></a>
	<h2>第九章 設立分會</h2>
	<ul>第一條: 本會可於國內外設立分會。</ul>
    <ul>第二條: 凡國內外與本會信仰相同之教會, 有意加入本會成為所屬分會, 可用書面向本會申請。 經本會長執會及會員大會三分之二通過, 才能接納。</ul>
	<ul>第三條: 申請歸屬本會之教會須遵守本會會章及接受本會長執會之指導。</ul>
	<ul>第四條: 本會有義務扶助分會靈性之長進及設立長執會。</ul>
	<ul>第五條: 分會應一律冠上地名。 稱為 "XXX中國基督教會"。</ul>
	<ul>第六條: 分會得自聘牧者, 惟應事先向本會報備。</ul>
	<ul>第七條: 分會財政獨立。 須直接向政府申請 E.I.N. (Employer Identification Number)。</ul>
	<ul>第八條: 分會應向本會報告一年來的事工及財務。</ul>
	<ul>第九條: 分會之不動產業購買、出售或轉移須經分會按會章通過, 並取得本會長執會三分之二通過書面批准, 才可實行。</ul>
	<ul>第十條: 分會如欲脫離本會, 應有分會會員三分之二通過,並經本會長執會三分之二通過書面批准, 始得脫離關係。</ul>


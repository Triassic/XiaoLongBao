	<ul>第四條: 本會章中英文各具乙份，但以中文為依據。</ul>
	<ul>第五條: 會友大會 投票會議，選票與選票計算方法
	  <ul>1. 法定人數為該會議有投票權會友的一半。</ul>
	  <ul>2. 出席投票會議且具投票權會友的人數須超過法定人數。</ul>
	  <ul>3. 所欲投票表決的提案，應只有兩個選擇∶贊成或反對。</ul>
	  <ul>4. 贊成者可在贊成方格打記號。反對者可在反對方格打記號. 不表明贊成或反對者可交回空白票。</ul>
	  <ul>5. 所收回的贊成票和反對票的縂數，稱為有效票數。</ul>
	  <ul>6. 有效票數須超過出席會議且具投票權會友的半數。若不具備這條件，該提案即視為已被否決。</ul>
	  <ul>7. 提案通過須贊成票與有效票數的比例符合會章規定。</ul>

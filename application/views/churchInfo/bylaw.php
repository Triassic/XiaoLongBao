	<a id="Chapter1"></a>
	<h2>第一章 總綱</h2>

	<ul>第一條：本教會定名為山景城中國基督教會(Mountain View Chinese Christian Church)</ul>
	<ul>第二條：本教會地址設於175 East Dana St, Mountain View, CA.</ul>
	<ul>第三條：本教會之信仰
		<ul>(l)我們信：全部新舊約聖經六十六卷均係上帝所默示，為我們信仰及生活唯一最高權威與指標。</ul>
		<ul>(2)我們信：上帝乃父、子、聖靈三位一體；是同質、同權、同尊、同榮；是自有永有唯一的真神。</ul>
		<ul>(3)我們信：得救是本乎恩，也因著信；耶穌基督道成肉身，為童貞女馬利亞從聖靈感孕所生，被釘十字架為人代死贖罪，三日後復活，升天，並要再來審判活人死人；
			凡靠主耶穌基督認罪悔改者，均蒙重生得救。</ul>
	</ul>
	<ul>第四條：本教會奉主耶穌基督為教會元首，以宣揚基督福音，領人歸主，聖徒各盡其職，肢體配搭服事，相顧相愛，建立基督的身體，完成主所託付之使命為職責。</ul>


	<h2>第五章 聖工推展之體制</h2>
	<ul>第一條：本教會秉承主耶穌基督吩咐：「你們要去，使萬民做我的門徒」(馬太28:19)之大使命，並由聖靈之引導，建立真神所喜悅之教會。</ul>
	<ul>第二條：教牧方面：本教會禮聘主任牧師一人，牧師傳道人若干人，為本教會各項聖工之指導，聘書 (Employment Contracts) 必須包括事工內容, 
		績效標準, 每年工作評估, 外出及假期規定。 聘書由長執會正副主席制訂。  教牧執掌如下：
	  <ul>(1)主任牧師為教牧同工之帶領者，負責教牧工作之分配，並輔導教牧之靈性及事奉。</ul>
	  <ul>(2)牧養全教會信徒，探訪會友及慕道友使他們靈命成長，並調解會友家庭問題。</ul>
	  <ul>(3)安排每主日及其他特別聚會講壇、聖餐、婚禮、喪禮之主持。</ul>
	  <ul>(4)輔導各長老執事之工作分配，推動長執會所決議聖工之推行與檢討。</ul>
	</ul>
	<ul>第三條：長老方面:
	  <ul>(1)本教會在必要時得設立長老若干人，配搭牧師們推動聖工，並協助教牧人員牧養會眾。</ul>
	  <ul>(2)本教會所禮聘之牧師為當然長老。</ul>
	  <ul>(3)長老資格：依據提前三章一至十三節及提多一章五至九節:
		<ul>A.本會會友，年在四十歲以上，曾受洗禮已逾五年，並做過執事同工兩年，熱誠服事有見証。</ul>
		<ul>B.信仰品行均端正，靈命充沛，善管自己的家，且得會友之信任愛戴。</ul>
	  </ul>
	  <ul>(4)長老之遴選：由教牧提名，先經長執會三分之二通過，在一個月前推荐會眾，並經會友大會三分之二以上表決通過後，由教會牧師們舉行按手禮；
	  	  所按立之長老任期四年，連選得連任，最長連續任期為十二年，連選辦法與首次遴選相同。</ul>
	  <ul>(5)牧長團 (牧師,傳道,及長老) 得舉行不定期聚會，由主任牧師擔任主席。</ul>
	</ul>
	<ul>第四條：執事方面：
	  <ul>(1)執事資格：依據使徒行傳六章一至六節、提前三章一至十三節。</ul>
	  <ul>(2)執事工作：依長執會分配，服事眾弟兄姊妹。</ul>
	  <ul>(3)執事產生：
		<ul>A.執事名額至少五人，任期兩年，每年梯次改任二分之一左右，最長連續任期為四年。</ul>
		<ul>B.執事人選由長執會提名，會友大會出席且具投票權會友過半數核定任命之。</ul>
		<ul>C.選舉前一個月，應將候選人名單公佈，以便會友為候選人代禱。</ul>
	  </ul>
	</ul>


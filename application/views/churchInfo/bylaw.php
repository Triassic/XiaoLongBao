	<a id="Chapter7"></a>
	<h2>第七章 受俸之規定</h2>
	<ul>第一條：本教會聘請主任牧師一位，先由長執會產生聘牧小組至少三人，物色適當人選，提經長執會通過後，推荐給會友大會，經三分之二以上投票贊成後，由教會以聘書禮聘之。
		任期滿後，雙方認為能繼續同心事奉，得由教會續聘。會友大會投票程序及計票規則, 參照會章附則第十章第五條。</ul>
	<ul>第二條：本教會聘請牧師傳道人若干人，由主任牧師提名，交由長執會通過後，推荐會友大會，經三分之二投票贊成後，由教會用聘書禮聘之；
		若主任牧師出缺，則由長執會主席召開長執會，產生聘牧小組，依照第七章第一條之程序聘請牧師或傳道人。其首次任期二年，任期滿後，雙方認為能繼續同心事奉，得由教會續聘，
	  	其任期由雙方商討之。會友大會投票程序及計票規則, 參照會章附則第十章第五條。</ul>
	<ul>第三條：本會傳道人可按立為牧師，由長執會及會友大會三分之二通過後，組成牧師團予以按立之。</ul>
	<ul>第四條：本教會若遇牧師出缺時，長執咸認會友中有弟兄被主呼召，並有牧養恩賜，經全教會懇切禱告，並由會友大會三分之二通過，予以按立為本教會牧師，
	  	其任期、薪俸參造本章第二、五條辦理之。</ul>
	<ul>第五條：本教會視聖工發展需要，需雇用職員，其聘請及管理之辦法由長執會另訂之。</ul>
	<ul>第六條：本教會牧師、傳道人及其他雇用職員，均應支給薪津，其各別薪津若干，則由長執會每年議訂之。</ul>
	<ul>第七條：凡支給薪津人員，自擔任職務之日起，均應按照政府規定，參加醫療保險，其繳付保險金額，全由本教會支付。</ul>
	<ul>第八條：凡支給薪津人員，在本教會工作滿五年以上者，因故辭職，應由本教會發給酬勞金，其金額多寡由長執會商定支給之。</ul>
	<ul>第九條：凡支給薪津人員，在本教會事奉十年以上而辭職者，或自願退休者，本教會發給退休金，其金額多寡由長執會商定支給之。</ul>
	<ul>第十條：本教會所按立之長老，均為無俸給之聖職。</ul>
	<ul>第十一條：本教會牧師、傳道人及職員在任職期間，如被發現其信仰上有違背真道、謬講聖經、隨從異端，或行為品格不正，或家庭破裂等嚴重問題，經查明屬實，
	  	由長執會提交會友大會三分之二通過，予以解聘或解雇。</ul>


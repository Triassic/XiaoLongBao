    <h2>第三章 會友大會</h2>
	<ul>第一條：會友大會為本教會最高的權力會議。</ul>
	<ul>第二條：
		<ul>A)年度會友大會：於四月份舉行。由長執會主席召開及主持一切。法定人數為具投票權會友一半以上之數，並由主席提出全年工作報告及檢討，又討論牧師、長執、及會友所提之議案。</ul>
		<ul>B)臨時會友大會：必要時經長執會通過後，得召開臨時會友大會。</ul>
	</ul>
	<ul>第三條：前條所列之會友大會及臨時會友大會，除本會章內另有規定外，概以現場出席會友過半數以上通過表決為原刖。</ul>


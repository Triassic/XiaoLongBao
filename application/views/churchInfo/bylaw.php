	<h2>第六章 長老執事會</h2>
	<ul>第一條：長老執事會由長老與執事所組成，簡稱長執會。本教會各項聖工有賴長執會推行，每年分工一次，其工作項目最長連續任期為四年，長執之分工如下：
	  <ul>(1)主席：由長執會推舉本會所按立長老中之一人擔任之。未設立長老前，選執事中一人擔任。其職責如下:
		<ul>A.擔任長執會議主席。</ul>
		<ul>B.負責長執間聖工之配搭與協調。</ul>
		<ul>C.配合教牧執行長執會議之決議案。</ul>
		<ul>D.教會應繼續舉辦之聖工籌劃與分配。</ul>
		<ul>E.會同主任牧師代表教會出席教會外有關教會之會議。</ul>
		<ul>F.當教牧及長老均虛懸時，代理執行一切職務。</ul>

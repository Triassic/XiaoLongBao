	<h2>第四章 禮儀</h2>
	<ul>第一條：本教會遵照聖經教導，設立以下兩種禮儀:
	  <ul>(1)洗禮：凡受聖靈感動，接受耶穌基督為救主而蒙重生得救，均可接受洗禮。由牧師主持，以表明已經與主同死、同葬、同復活之見証。</ul>
	  <ul>(2)主餐：由長執會規定日期，在主日崇拜中舉行，記念主耶穌為我們捨身流血贖罪之救恩，該禮儀由牧師主持，全體信徒均可分領餅與杯。</ul>
	</ul>


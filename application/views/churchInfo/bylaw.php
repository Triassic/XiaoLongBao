	<h2>第二章 會籍</h2>
	<ul>第一條：合於下列各項之一者，皆可加入本教會為會友：
	  <ul>(1)凡在本教會決志信主，經本教會牧師、長執考察其信仰言行純正，確實蒙恩得救，並接受洗禮後，均為本教會會友。</ul>
	  <ul>(2)凡與本教會信仰相同的基督徒，如願加入本教會，且已陸續參加本教會主日崇拜達三個月以上者，經本會長執會認可通過後，可接納為本教會會友。</ul>
	  <ul>(3)本教會聘任之牧師及傳道人及其配偶為本會當然會友。</ul>
	</ul>
	<ul>第二條：投票權：
	  <ul>凡會友具有下列情況之一者，不得享有會友大會之投票權：
	    <ul>（ｌ）在召開會友大會之日不足十八歲。</ul>
	    <ul>（２）在召開會友大會之前，長執會認定該會友未克經常出席主日聚會。</ul>
	  </ul>
	</ul>

	<ul>第三條：會籍之終止：
	  <ul>(l)凡自動退出或轉去其他教會之會友，經由長執會認可後，其在本教會會友身份即告終止。</ul>
	  <ul>(2)凡會友言行有嚴重違反聖經教訓或本教會會章者，曾經牧長勸告無效，由長執會表決後停止其會籍。</ul>
	</ul>

